import json
import os

import joblib
import numpy as np
from flask import Flask, render_template, request

# ── App Initialisation ─────────────────────────────────────────────────────────
app = Flask(__name__)

BASE_DIR = os.path.dirname(os.path.abspath(__file__))

# ── Load ML Assets ─────────────────────────────────────────────────────────────
model  = joblib.load(os.path.join(BASE_DIR, 'best_model.pkl'))
scaler = joblib.load(os.path.join(BASE_DIR, 'scaler.pkl'))

with open(os.path.join(BASE_DIR, 'feature_columns.json')) as f:
    FEATURE_COLS = json.load(f)   # exact column order used during training

print(f"✔ Model loaded  : {type(model).__name__}")
print(f"✔ Scaler loaded : {type(scaler).__name__}")
print(f"✔ Features ({len(FEATURE_COLS)}): {FEATURE_COLS}")


# ── Smoking history OHE columns the model might expect ────────────────────────
# (generated by pd.get_dummies — not all are necessarily in FEATURE_COLS)
ALL_SMOKING_OHE = [
    'smoking_history_current',
    'smoking_history_ever',
    'smoking_history_former',
    'smoking_history_never',
    'smoking_history_no_info',
    'smoking_history_not current',
]


# ── Helper ─────────────────────────────────────────────────────────────────────
def build_input_vector(form) -> np.ndarray:
    """
    Convert raw HTML form values into the exact feature vector
    expected by the trained model (FEATURE_COLS order, OHE smoking).
    """
    # --- raw numeric inputs ---
    gender_raw      = form.get('gender', 'Male')          # 'Male' / 'Female' / 'Other'
    age             = float(form.get('age', 0))
    hypertension    = int(form.get('hypertension', 0))
    heart_disease   = int(form.get('heart_disease', 0))
    smoking_raw     = form.get('smoking_history', 'never')
    bmi             = float(form.get('bmi', 0))
    hba1c           = float(form.get('HbA1c_level', 0))
    glucose         = float(form.get('blood_glucose_level', 0))

    # --- gender label encoding (same as LabelEncoder fit on ['Female','Male','Other']) ---
    gender_map = {'Female': 0, 'Male': 1, 'Other': 2}
    gender_enc = gender_map.get(gender_raw, 1)

    # --- OHE for smoking_history ---
    smoking_key = f'smoking_history_{smoking_raw}'
    smoking_ohe = {col: 1 if col == smoking_key else 0 for col in ALL_SMOKING_OHE}

    # --- assemble full raw dict ---
    raw = {
        'gender':              gender_enc,
        'age':                 age,
        'hypertension':        hypertension,
        'heart_disease':       heart_disease,
        'bmi':                 bmi,
        'HbA1c_level':         hba1c,
        'blood_glucose_level': glucose,
    }
    raw.update(smoking_ohe)

    # --- keep only features the model was trained on, in correct order ---
    vector = np.array([[raw.get(col, 0) for col in FEATURE_COLS]], dtype=float)

    # --- scale ---
    vector_scaled = scaler.transform(vector)

    return vector_scaled


# ── Routes ─────────────────────────────────────────────────────────────────────
@app.route('/')
def index():
    """Report / Dashboard page."""
    return render_template('index.html')


@app.route('/predict', methods=['GET', 'POST'])
def predict():
    """Prediction form (GET) and result (POST)."""
    if request.method == 'GET':
        return render_template('predict.html')

    # POST — process form submission
    try:
        X = build_input_vector(request.form)
        prediction   = int(model.predict(X)[0])
        probabilities = model.predict_proba(X)[0]          # [P(0), P(1)]
        confidence   = round(float(probabilities[prediction]) * 100, 1)

        result_label = 'Diabetic' if prediction == 1 else 'Not Diabetic'
        risk_level   = (
            'High Risk'   if prediction == 1 and confidence >= 80 else
            'Moderate Risk' if prediction == 1 else
            'Low Risk'    if confidence >= 80 else
            'Borderline'
        )

        return render_template(
            'result.html',
            prediction=prediction,
            result_label=result_label,
            confidence=confidence,
            risk_level=risk_level,
            form_data=request.form,
        )

    except Exception as exc:
        return render_template(
            'predict.html',
            error=f"Prediction failed: {exc}. Please check your inputs."
        )


# ── Entry Point ────────────────────────────────────────────────────────────────
if __name__ == '__main__':
    app.run(debug=True)
